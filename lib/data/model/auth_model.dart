class UserData {
  String email;
  String firstName;
  String lastName;
  String mobile;
  String password;
  String photo;
  String createdDate;
  String id;

  UserData({
    required this.email,
    required this.firstName,
    required this.lastName,
    required this.mobile,
    required this.password,
    required this.photo,
    required this.createdDate,
    required this.id,
  });
}
